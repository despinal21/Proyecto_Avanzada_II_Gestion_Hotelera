private void cargarReservas() {
    
    String[] titulos = {"RESERVA", "HUESPED", "HABITACION", "ENTRADA", "SALIDA", "ESTADO"};
    javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(null, titulos);
    String sql = "SELECT * FROM reservas";

    try (Connection cn = con.estableceConexion();
         PreparedStatement pst = cn.prepareStatement(sql);
         ResultSet rs = pst.executeQuery()) {
            while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }

        while (rs.next()) {
            Object[] fila = new Object[6]; 
            fila[0] = rs.getString("id_reserva");
            fila[1] = rs.getString("id_huesped");
            fila[2] = rs.getString("id_habitacion");
            fila[3] = rs.getString("fecha_entrada");
            fila[4] = rs.getString("fecha_salida");
            fila[5] = rs.getString("estado");
            modelo.addRow(fila);
        }
        
        
        jTable1.setModel(modelo);
        
    } catch (SQLException e) {
        
        e.printStackTrace();
        System.err.println("Error al cargar las reservas: " + e.getMessage());
    }
}

