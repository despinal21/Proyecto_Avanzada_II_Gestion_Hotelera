    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int filaSeleccionada = jTable1.getSelectedRow();
if (filaSeleccionada == -1) {
    JOptionPane.showMessageDialog(this, "Por favor, seleccione una reserva para eliminar.");
    return;
}

int confirmacion = JOptionPane.showConfirmDialog(this, "¿Está seguro de que desea eliminar esta reserva?");
if (confirmacion == JOptionPane.YES_OPTION) {
    try {
       
        String idReserva = jTable1.getValueAt(filaSeleccionada, 0).toString();

        
        String sql = "DELETE FROM reservas WHERE id_reserva = ?";
        Connection cn = con.estableceConexion();
        PreparedStatement pst = cn.prepareStatement(sql);
        pst.setString(1, idReserva);
        pst.executeUpdate();
        
        JOptionPane.showMessageDialog(this, "Reserva eliminada exitosamente.");
        cargarReservas(); 
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al eliminar la reserva: " + e.getMessage());
    }
}
    }//GEN-LAST:event_jButton3ActionPerformed

