public void guardarHuesped() {
    String idString = jTextField6.getText();
    String nombre = jTextField2.getText();
    String apellido = jTextField1.getText();
    String correo = jTextField3.getText();
    String telefono = jTextField4.getText();
    String direccion = jTextArea1.getText();

    if (nombre.isEmpty() || apellido.isEmpty() || correo.isEmpty() || telefono.isEmpty() || direccion.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.");
        return;
    }

    Conexion conexion = new Conexion();
    Connection con = conexion.estableceConexion();
    PreparedStatement ps = null;

    try {
        if (idString.isEmpty()) { 
            String sql = "INSERT INTO huespedes (nombre, apellido, correo_electronico, telefono, direccion) VALUES (?, ?, ?, ?, ?)";
            ps = con.prepareStatement(sql);
            ps.setString(1, nombre);
            ps.setString(2, apellido);
            ps.setString(3, correo);
            ps.setString(4, telefono);
            ps.setString(5, direccion);
        } else { 
            String sql = "UPDATE huespedes SET nombre=?, apellido=?, correo_electronico=?, telefono=?, direccion=? WHERE id_huesped=?";
            ps = con.prepareStatement(sql);
            ps.setString(1, nombre);
            ps.setString(2, apellido);
            ps.setString(3, correo);
            ps.setString(4, telefono);
            ps.setString(5, direccion);
            ps.setLong(6, Long.parseLong(idString));    
        }

        int filasAfectadas = ps.executeUpdate();

        if (filasAfectadas > 0) {
            JOptionPane.showMessageDialog(this, "Hu√©sped guardado exitosamente.");
            limpiarCampos();
            
            huespedesParent.mostrarHuespedes();
            this.dispose();
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error al guardar el hu√©sped: " + e.getMessage());
    } finally {
        try {
            if (ps != null) ps.close();
            if (con != null) con.close();
        } catch (SQLException e) {
            System.out.println("Error al cerrar la conexi√≥n: " + e.getMessage());
        }
    }
}

